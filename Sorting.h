// Sorting.h - Header file for sorting functions related to appointment management.

#ifndef SORTING_H
#define SORTING_H

// Function prototypes for sorting appointments.

/**
 * Sorts appointments by date.
 */
void sortByDate();

/**
 * Sorts appointments by date and time.
 */
void sortByDateTime();

/**
 * Sorts appointments by date and priority.
 */
void sortByDatePriority();

/**
 * Sorts appointments by location, priority, and duration in descending order.
 */
void sortByLocationPriorityDuration();

#endif // SORTING_H
